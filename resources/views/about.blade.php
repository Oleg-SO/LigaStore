  <p><h2 >Что такое ТЭМП</h2><p>
        <p>Свою историю компания начинает в 1957 году на базе Троицкого ЭлектроМеханического Предприятия (Т.Э.М.П). В 1957 г. мы начали производство дрелей и углошлифовальных машин, и уже в 1970 г. номенклатура предприятия насчитывала 15 наименований продукции. Основные потребители продукции Т.Э.М.П был военно-промышленный комплекс — 90% продукции производилось под госзаказ оборонки. В 1985 году предприятие столкнулось с системным кризисом в экономике СССР и производство электроинструмента было приостановлено. В 1999 г. начал активно развиваться рынок инструмента в России, что и послужило возрождению торговой марки Т.Э.М.П. К счастью нам удалось найти и сплотить вокруг единой цели инженеров завода - ведь не секрет, что инженеры в СССР были самыми квалифицированными в мире.></p>
        <p>С 1997 года в Китае было размещено производство таких инструментальных брендов как MAKITA, BOSCH, DEWALT. Как следствие Китай получил технологии производства инструмента и уже к 2000 году «поднебесная» стала лидером в производстве инструмента.</p>
        <p>Компания ТЕМП-TOOLS с 2000 г. начинает активно искать производственную базу в Китае – потому что производить в Китае намного дешевле в силу развитости этой индустрии.></p>
  <hr>

  <p><h2>Производственная база</h2><p>
        <p>2000 год можно считать годом возрождения торговой марки Т.Э.М.П - инженеры компании начинают проектные работы по созданию линии электроинструмента и уже в 2002 году компания начинает производство инструментов торговой марки «ТЭМП» – надежного инструмента по доступной цене для широкого применения в строительстве.</p>
        <p>После успешного тестирования и сертификации по мировым стандартам ISO9001 в 2003 г. компания выводит первые 33 позиции торговой марки «ТЭМП». К 2007 году продукция торговой марки «ТЭМП» представлена на рынках Украины, Казахстана и России. В 2008 году начался мировой экономический кризис, но наша компания смогла не только выстоять, но и с помощью оптимизации хозяйственной деятельности не сойти с пути развития компании.</p>
        <p>Уже к 2009 г. ассортимент торговой марки «ТЭМП» насчитывал 70 позиций. Вследствие сокращения строительства спрос на электроинструмент снижался и руководство компании приняло решение по созданию торговой марки садового инструмента – так появилась торговая марка «FERMER». Зa 2009 г. садовая техника нашей компании зарекомендовала себя на рынках СНГ как надежный инструмент.</p>
        <p>В 2010 г. наша компания продолжает политику наращивания ассортимента и усовершенствования контроля качества нашей продукции, мы расширяем на ряд позиций срок гарантии до 24 месяцев – тем самым подчеркивая повышенное качество этих позиций. На рынок выходят новинки торговой марки «ТЭМП» – инверторная сварка, насосы, новый перфоратор, а также компания начинает активно выводить на рынок Li-ion - технологии. Все инструменты комплектуются Li-ion батареями компании TDK (Япония).</p>
        <p>Также в 2010 г. наша компания выводит на рынок высококачественные расходные материалы: сверла, буры, диски, пильные цепи и шины. Весной 2010 г. на рынок поступают мотокультиваторы и мотоблоки торговой марки «FERMER».</p>
        <p>Также в 2010 г. наша компания начинает старт с новой торговой маркой «KASUMI» – ручной инструмент. Торговая марка «KASUMI» - это автомобильный ручной инструмент и оборудование для автомастерских. Бренд позиционирует себя на рынке как профессиональный инструмент и оборудование японского качества и рассчитан на широкий круг потребителей от автолюбителей до шиномонтажных мастерских.</p>
        <p>На весь ручной инструмент торговой марки «KASUMI» производитель дает ПОЖИЗНЕННУЮ ГАРАНТИЮ!!!</p>
        <p>В 2011 г. торговые бренды компании ТЭМП и FERMER насчитывают уже более 100 позиций, а также срок гарантии на некоторые из этих позиций увеличен до 3-х лет. С 2012 г. компания начинает старт с новой торговой марки «KRAFTER» - бензиновые генераторы.</p>

